void FoundationConcepts::seed_all(LongTermMemory& ltm) {
    g_lmap.clear();
    seed_tier1_ontology(ltm);
    seed_tier2_categories(ltm);
    seed_tier4_science(ltm);
    seed_tier3_relations(ltm);
}

void FoundationConcepts::seed_tier1_ontology(LongTermMemory& ltm) {
    store_seeds(ltm, T1, g_lmap);
}

void FoundationConcepts::seed_tier2_categories(LongTermMemory& ltm) {
    store_seeds(ltm, T2, g_lmap);
}

void FoundationConcepts::seed_tier3_relations(LongTermMemory& ltm) {
    store_rels(ltm, R3, g_lmap);
}

void FoundationConcepts::seed_tier4_science(LongTermMemory& ltm) {
    store_seeds(ltm, T4, g_lmap);
}

size_t FoundationConcepts::concept_count() {
    return T1.size() + T2.size() + T4.size();
}

size_t FoundationConcepts::relation_count() {
    return R3.size();
}

