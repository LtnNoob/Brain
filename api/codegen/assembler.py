"""Brain19 Code Assembly Engine.

Takes a pipeline sequence (ordered step IDs + parameter overrides) and
generates a complete main.cpp with correct includes, initialization, and
steps in the right order.
"""

from __future__ import annotations

import re
from dataclasses import dataclass, field

from .templates import TEMPLATES, StepTemplate


@dataclass
class PipelineStep:
    step_id: str
    enabled: bool = True
    params: dict[str, str] = field(default_factory=dict)


@dataclass
class PipelineConfig:
    steps: list[PipelineStep] = field(default_factory=list)
    data_dir: str = "brain19_data/"
    enable_persistence: bool = True
    seed_foundation: bool = True


# ---------------------------------------------------------------------------
# Code generation
# ---------------------------------------------------------------------------

