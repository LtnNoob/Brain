def _substitute_params(code: str, params: dict[str, str], template: StepTemplate) -> str:
    """Replace {{param}} placeholders with actual values."""
    # Build a complete param map: defaults first, then overrides
    param_map: dict[str, str] = {}
    for p in template.params:
        param_map[p.name] = p.default
    param_map.update(params)

    for name, value in param_map.items():
        code = code.replace(f"{{{{{name}}}}}", value)

    return code


def _collect_includes(steps: list[PipelineStep]) -> str:
    """Collect unique includes from all enabled steps."""
    seen: set[str] = set()
    lines: list[str] = []
    for ps in steps:
        if not ps.enabled:
            continue
        tmpl = TEMPLATES.get(ps.step_id)
        if not tmpl:
            continue
        for inc in tmpl.includes:
            if inc not in seen:
                seen.add(inc)
                lines.append(f"#include {inc}")
    return "\n".join(lines)


def _build_config_overrides(steps: list[PipelineStep]) -> str:
    """Build config override lines from pipeline parameters."""
    lines: list[str] = []

    # Map specific params to config fields
    param_config_map = {
        "initial_activation": "config.thinking_config.initial_activation",
        "top_k_salient": "config.thinking_config.top_k_salient",
        "max_relevance_maps": "config.thinking_config.max_relevance_maps",
        "enable_understanding": "config.thinking_config.enable_understanding",
        "enable_kan_validation": "config.thinking_config.enable_kan_validation",
        "enable_curiosity": "config.thinking_config.enable_curiosity",
        "enable_focus_cursor": "config.thinking_config.enable_focus_cursor",
        "enable_gdo": "config.enable_gdo",
    }

    for ps in steps:
        if not ps.enabled:
            continue
        tmpl = TEMPLATES.get(ps.step_id)
        if not tmpl:
            continue
        for p in tmpl.params:
            val = ps.params.get(p.name, p.default)
            config_field = param_config_map.get(p.name)
            if config_field:
                lines.append(f"    {config_field} = {val};")

    return "\n".join(lines) if lines else "    // (using defaults)"


def generate_main_cpp(pipeline: PipelineConfig) -> str:
    """Generate a complete main.cpp from a pipeline configuration."""
    includes = _collect_includes(pipeline.steps)
    config_overrides = _build_config_overrides(pipeline.steps)

    header = _MAIN_HEADER.format(includes=includes)
    body = _MAIN_PREAMBLE.format(
        data_dir=pipeline.data_dir,
        enable_persistence=str(pipeline.enable_persistence).lower(),
        seed_foundation=str(pipeline.seed_foundation).lower(),
        config_overrides=config_overrides,
    )

    return header + body


def parse_current_pipeline(main_cpp_content: str) -> list[dict]:
    """Parse an existing main.cpp and extract the pipeline step sequence.

    Returns a list of step descriptors inferred from comments and code patterns.
    """
    steps: list[dict] = []

    # Match step comments like "// Step 1:", "// Step 2:", "// Step 2.5:", etc.
    step_pattern = re.compile(
        r'//\s*Step\s+(\d+(?:\.\d+)?)\s*:\s*(.+)', re.IGNORECASE
    )

    # Also match config assignments
    config_pattern = re.compile(
        r'config\.thinking_config\.(\w+)\s*=\s*(.+?)\s*;'
    )

    params: dict[str, str] = {}
    for line in main_cpp_content.split("\n"):
        m = config_pattern.search(line)
        if m:
            params[m.group(1)] = m.group(2)

    for line in main_cpp_content.split("\n"):
        m = step_pattern.search(line)
        if m:
            step_num = m.group(1)
            label = m.group(2).strip()

            # Map step numbers to template IDs
            step_id = _step_number_to_id(step_num)
            if step_id:
                steps.append({
                    "step_id": step_id,
                    "step": step_num,
                    "label": label,
                    "enabled": True,
                })

    # Check for optional steps that might be disabled
    for step_id, tmpl in TEMPLATES.items():
        found = any(s["step_id"] == step_id for s in steps)
        if not found and tmpl.step != "BG":
            steps.append({
                "step_id": step_id,
                "step": tmpl.step,
                "label": tmpl.label,
                "enabled": False,
            })

    return steps


def _step_number_to_id(num: str) -> str | None:
    mapping = {
        "1": "stm",
        "2": "spreading",
        "2.5": "cursor",
        "3": "salience",
        "4": "relevance",
        "4-5": "relevance",
        "5": "relevance",
        "6": "paths",
        "7": "curiosity",
        "8": "understanding",
        "9": "kan",
    }
    return mapping.get(num)
