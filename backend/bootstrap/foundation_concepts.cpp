struct ConceptSeed {
    std::string label;
    std::string definition;
    EpistemicType type;
    double trust;
};

struct RelationSeed {
    std::string source_label;
    std::string target_label;
    RelationType type;
    double weight;
};

void store_seeds(LongTermMemory& ltm,
                 const std::vector<ConceptSeed>& seeds,
                 std::unordered_map<std::string, ConceptId>& lmap) {
    for (const auto& s : seeds) {
        auto id = ltm.store_concept(s.label, s.definition,
            EpistemicMetadata(s.type, EpistemicStatus::ACTIVE, s.trust));
        lmap[s.label] = id;
    }
}

void store_rels(LongTermMemory& ltm,
                const std::vector<RelationSeed>& seeds,
                const std::unordered_map<std::string, ConceptId>& lmap) {
    for (const auto& r : seeds) {
        auto si = lmap.find(r.source_label);
        auto ti = lmap.find(r.target_label);
        if (si != lmap.end() && ti != lmap.end())
            ltm.add_relation(si->second, ti->second, r.type, r.weight);
    }
}

